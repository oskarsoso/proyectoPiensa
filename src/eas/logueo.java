    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        user=txtusuario.getText().trim(); // el trim elimina los espacios en blanco que se dejan al poner el usuario
        pass=txtpassword.getText().trim();
        
        if(!user.equals("") || !pass.equals("")){
            try {
                Connection cn = Conexion.conectar(); //vincular con la clase coneccion
                PreparedStatement pst = cn.prepareStatement("select tiponivel, status from usuarios where username='"+user+"' && password='"+pass+"' ");
                ResultSet rs = pst.executeQuery();
                
                if(rs.next()){
                    String tiponivel = rs.getString("tiponivel"); // toma el valor de la tabla usuarios
                    String status = rs.getString("status");
                    
                    if(tiponivel.equalsIgnoreCase("Administrador") && status.equalsIgnoreCase("Activo")){ //no discrimine mayusculas de minisculas
                        dispose(); //Cierra este formulario
                        new menu().setVisible(true); //Abre el otro formulario
                        
                    
                        
                    }else{
                        JOptionPane.showMessageDialog(null, "Credenciales Incorrectas");
                        txtusuario.setText("");
                        txtpassword.setText("");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al iniciar la sesi√≥n, contactese con el Administrador");
            } 
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe llenar los campos");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    
